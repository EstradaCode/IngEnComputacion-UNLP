package Trees;

public class StringNum {
	private String letra;
	private int num;
	
	public StringNum(String letra, int num) {
		this.letra = letra;
		this.num = num;
	}
	public String getLetra() {
		return letra;
	}
	public void setLetra(String letra) {
		this.letra = letra;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
