package Graphs;

public class FaseCiudad {
	private int fase;
	private String nombre;
	public FaseCiudad(int fase, String nombre) {
		super();
		this.fase = fase;
		this.nombre = nombre;
	}
	public int getFase() {
		return fase;
	}
	public void setFase(int fase) {
		this.fase = fase;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	

}
