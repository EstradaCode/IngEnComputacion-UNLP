ORG 1000H
TABLA DB 1,20,5,4,30
CONT DB 0
ORG 3000H
SUBRUTINA: PUSH BX
MOV BX, SP 
ADD BX, 4
MOV BX,[BX]; TENGO OFFSET N 
PUSH AX
MOV AH,0;AH SERÁ RES, AL SERÁ I
MOV AL,0
HACER: INC AL
ADD AH, AL
CMP AL, [BX]
JNZ HACER
FIN: MOV [BX], AH
POP AX
POP BX
RET

ORG 2000H
MOV BX, OFFSET TABLA
MOV AL, CONT
BUCLE:PUSH BX
CMP BYTE PTR [BX], 20
JNS SIGO
CALL SUBRUTINA
INC AL; UNA VEZ AFUERA PUEDO ASEGURAR QUE EL VALOR FUE MODIFICADO
SIGO: POP BX
INC BX
CMP BX, OFFSET CONT
JNZ BUCLE
MOV CONT, AL
HLT
END