ORG 1000H
TABLA DB 1,5,6,8,29,69
ROTACIONES DB 4
ORG 3000H
ROTARIZQ: PUSH BX
PUSH AX; LO UTILIZO
MOV BX,SP
ADD BX, 6
MOV BX,[BX]; OBTIENE LA REFERENCIA
MOV AH, [BX]; OBTIENE EL VALOR DE LA TABLA
SIGO: CMP AL,0
JZ FIN
ADD AH,AH
ADC AH, 0
DEC AL
JMP SIGO
FIN: MOV [BX],AH
POP AX
POP BX
RET 

ORG 2000H
MOV BX, OFFSET TABLA
MOV CL, OFFSET ROTACIONES-OFFSET TABLA
MOV AL, ROTACIONES; POR VALOR VIA REGISTRO
BUCLE: PUSH BX
CALL ROTARIZQ
POP BX
INC BX
DEC CL
JNZ BUCLE
HLT
END