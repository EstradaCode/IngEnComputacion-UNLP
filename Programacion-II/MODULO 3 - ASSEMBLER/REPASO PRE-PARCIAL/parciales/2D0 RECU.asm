ORG 1000H
TABLAA DW 10,12,3,4,50 ;REGISTRO
TABLAB DW 1,20,15,3,35 ;PILA
CONT DB 0
LIM EQU 10

ORG 3000H
SUBRUTINA: PUSH BX
MOV BX, SP
ADD BX,4
MOV BX, [BX]
PUSH CX
PUSH AX
PUSH DX
MOV AX, 0
MOV DX, 0
IF: CMP WORD PTR [BX], LIM
JNS ELSE
MOV AL,0
FOR: PUSH BX
MOV BX, CX
MOV DX, [BX]
ADD [BX], DX
POP BX
INC AL
CMP AL, [BX]
JNZ FOR
JMP FIN_SUB
ELSE: MOV AX, [BX]; TIENE A
PUSH BX
MOV BX, CX
MOV DX, [BX]; TIENE B
POP BX
MOV [BX],DX
ADD [BX],AX
FIN_SUB: POP DX
POP AX
POP CX
POP BX
RET


ORG 2000H
MOV BX, OFFSET TABLAA
MOV CX, OFFSET TABLAB
MOV AL, OFFSET TABLAB- OFFSET TABLAA 
MOV AH,0
BUCLE: PUSH BX
CALL SUBRUTINA
MOV BX, CX
MOV BX, [BX]
CMP BX, LIM + 1
JS SIGO
INC AH
SIGO:POP BX
ADD BX, 2
ADD CX,2
SUB AL, 2
JNZ BUCLE
MOV CONT, AH
HLT 
END